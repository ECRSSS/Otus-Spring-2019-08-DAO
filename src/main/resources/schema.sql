ALTER TABLE "AUTHORS_BOOKS" ADD FOREIGN KEY ("BOOK_ID") REFERENCES "BOOKS" ("ID");

ALTER TABLE "GENRES_BOOKS" ADD FOREIGN KEY ("BOOK_ID") REFERENCES "BOOKS" ("ID");

